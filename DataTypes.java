public class DataTypes
{
    public static void main(String[] args) {
        //primitive data types are also built-in reserved on different variable
        int number=1;//for storing whole numbers
        double num=1.22334;//also for storing decimal numbers
        String name="dosta";//for storing text
       char alphabet='A';//for single character
       System.out.println(number); 
       System.out.println(num);
       System.out.println(name);
       System.out.println(alphabet);
    }
}